  //
  const backUrul = import.meta.env.VITE_AUTH_API_URL;


  const navigate = useNavigate(); // Hook to programmatically navigate

  // Function to validate email format
  const validateEmail = (email) => {
    const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return re.test(email);
  };
